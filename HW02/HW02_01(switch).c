#include<stdio.h>

int main(void) {
	double x, fax;
	scanf("%lf", &x);
	/*获取x的首位数*/
	int num = (int)x / 1000;
	/*主循环*/
	while (x != 0) {
		/*根据要求用switch语句计算税金*/
		switch (num) {
		case 0:
			fax = 0;
			break;
		case 1:
			fax = (x - 1000) * 0.05;
			break;
		case 2:
			fax = (x - 2000) * 0.1 + 1000 * 0.05;
			break;
		case 3:
			fax = (x - 3000) * 0.15 + 1000 * (0.1 + 0.05);
			break;
		case 4:
			fax = (x - 4000) * 0.2 + 1000 * (0.15 + 0.1 + 0.05);
			break;
		default:
			fax = (x - 5000) * 0.25 + 1000 * (0.2 + 0.15 + 0.1 + 0.05);
			break;
		}
		/*输出税金并输入下一轮数据*/
		printf("%lf\n", fax);
		scanf("%lf", &x);
		num = (int)x / 1000;
	}
	return 0;
}