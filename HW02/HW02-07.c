#include <stdio.h>

void main(void)
{
	int i = 1, n, s = 1;
	printf("Please enter n:");
	scanf("%d", &n);
	/*用do-while语句循环计算s*/
	do {
		s *= i;
		i++;
	} while (i <= n);
	printf("%d! = %d", n, s);
	return 0;
}